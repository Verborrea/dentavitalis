		<h2>Recopilación y uso de sus datos personales</h2>
		<h3>Tipos de datos recopilados</h3>
		<h4>Datos personales</h4>
		<p>Al utilizar Nuestro Servicio, podemos pedirle que nos
			proporcione cierta información de identificación personal que puede usarse para contactarlo o
			identificarlo. La información de identificación personal puede incluir, pero no se limita a:</p>
		<ul>
			<li>
				<p>Dirección de correo electrónico</p>
			</li>
			<li>
				<p>Nombre y apellidos</p>
			</li>
			<li>
				<p>Número de teléfono</p>
			</li>
			<li>
				<p>Datos de uso</p>
			</li>
		</ul>
		<h4>Datos de uso</h4>
		<p>Los Datos de uso se recopilan automáticamente al utilizar el
			Servicio.</p>
		<p>Los Datos de uso pueden incluir información como la
			dirección de Protocolo de Internet de Su Dispositivo (por ejemplo, dirección IP), tipo de
			navegador, versión del navegador, las páginas de nuestro Servicio que visita, la hora y fecha de
			Su visita, el tiempo dedicado a esas páginas, identificadores únicos de dispositivos y otros
			datos de diagnóstico.</p>
		<p>Cuando accede al Servicio mediante o a través de un dispositivo móvil, podemos recopilar cierta información automáticamente, que incluye, entre otros, el tipo de dispositivo móvil que utiliza, la identificación única de su dispositivo móvil, la dirección IP de su dispositivo móvil, su sistema operativo, el tipo de navegador de Internet móvil que utiliza, identificadores únicos de dispositivo y otros datos de diagnóstico.</p>
		<p>También podemos recopilar información que su navegador envía cada vez que visita nuestro Servicio o cuando accede al Servicio mediante un dispositivo móvil.</p>
		<h3>Uso de sus datos personales</h3>
		<p>La Compañía puede utilizar los Datos Personales para los
			siguientes propósitos:</p>
		<ul>
			<li>
				<p><strong>Para proporcionar y
						mantener</strong> nuestro Servicio, incluso para monitorear el uso de nuestro
					Servicio.</p>
			</li>
			<li>
				<p><strong>Cuenta: para gestionar Su</strong> registro como usuario del Servicio. Los Datos Personales
					que Usted proporciona pueden darle acceso a diferentes funcionalidades del Servicio que
					están disponibles para Usted como usuario registrado.</p>
			</li>
			<li>
				<p><strong>Para la ejecución de un contrato:</strong> el desarrollo, cumplimiento y realización del contrato de compra
					de los productos, artículos o servicios que ha adquirido o de cualquier otro contrato
					con Nosotros a través del Servicio.</p>
			</li>
			<li>
				<p><strong>Para ponernos en contacto con usted:</strong> Para contactarlo por correo electrónico, llamadas
					telefónicas, SMS u otras formas equivalentes de comunicación electrónica, como
					notificaciones push de una aplicación móvil sobre actualizaciones o comunicaciones
					informativas relacionadas con las funcionalidades, productos o servicios contratados,
					incluidas las actualizaciones de seguridad, cuando sea necesario o razonable para su
					implementación.</p>
			</li>
			<li>
				<p><strong>Para proporcionarle</strong> noticias, ofertas especiales e información general sobre
					otros bienes, servicios y eventos que ofrecemos que son similares a los que ya ha
					comprado o consultado, a menos que haya optado por no recibir dicha información.</p>
			</li>
			<li>
				<p><strong>Para gestionar sus solicitudes:</strong> Para atender y gestionar sus solicitudes a nosotros.</p>
			</li>
			<li>
				<p><strong>Para transferencias de negocios:</strong> Podemos usar su información para evaluar o llevar a cabo una
					fusión, desinversión, reestructuración, reorganización, disolución u otra venta o
					transferencia de algunos o todos nuestros activos, ya sea como una empresa en marcha o
					como parte de una quiebra, liquidación o procedimiento similar, en el que los datos
					personales que tenemos sobre los usuarios de nuestro servicio se encuentran entre los
					activos transferidos.</p>
			</li>
			<li>
				<p><strong>Para otros fines:</strong> Podemos utilizar su información
					para otros fines, como el análisis de datos, la
					identificación de tendencias de uso, la determinación de la efectividad de nuestras
					campañas promocionales y para evaluar y mejorar nuestro Servicio, productos, servicios,
					marketing y su experiencia.</p>
			</li>
		</ul>
		<p>Podemos compartir su información personal en las siguientes
			situaciones:</p>
		<ul>
			<li><strong>Con proveedores de servicios:</strong> Podemos compartir su información personal con proveedores de
				servicios para monitorear y analizar el uso de nuestro servicio, para contactarlo.</li>
			<li><strong>Para transferencias de negocios:</strong> Podemos compartir o transferir su información personal en relación
				con, o durante las negociaciones de, cualquier fusión, venta de activos de la Compañía,
				financiamiento o adquisición de todo o una parte de Nuestro negocio a otra compañía.</li>
			<li><strong>Con Afiliados:</strong>
				Podemos compartir su información con nuestros afiliados, en cuyo caso requeriremos que esos
				afiliados cumplan con esta Política de privacidad. Los afiliados incluyen nuestra empresa
				matriz y cualquier otra subsidiaria, socios de empresas conjuntas u otras compañías que
				controlamos o que están bajo control común con nosotros.</li>
			<li><strong>Con socios comerciales:</strong> Podemos compartir su información con nuestros socios comerciales
				para ofrecerle ciertos productos, servicios o promociones.</li>
			<li><strong>Con otros usuarios:</strong>
				cuando comparte información personal o interactúa de otra manera en las áreas públicas con
				otros usuarios, dicha información puede ser vista por todos los usuarios y puede
				distribuirse públicamente fuera de él.</li>
			<li><strong>Con su consentimiento:</strong> Podemos divulgar su información
				personal para cualquier otro propósito con su
				consentimiento.</li>
		</ul>
		<h3>Retención de sus datos personales</h3>
		<p>La Compañía conservará sus datos personales solo durante el
			tiempo que sea necesario para los fines establecidos en esta Política de privacidad.
			Conservaremos y utilizaremos sus datos personales en la medida necesaria para cumplir con
			nuestras obligaciones legales (por ejemplo, si estamos obligados a conservar sus datos para
			cumplir con las leyes aplicables), resolver disputas y hacer cumplir nuestros acuerdos y
			políticas legales.</p>
		<p>La Compañía también conservará los Datos de uso para fines
			de análisis interno. Los datos de uso generalmente se conservan por un período de tiempo más
			corto, excepto cuando estos datos se utilizan para fortalecer la seguridad o para mejorar la
			funcionalidad de nuestro servicio, o estamos legalmente obligados a retener estos datos durante
			períodos de tiempo más largos.</p>
		<h3>Transferencia de sus datos personales</h3>
		<p>Su información, incluidos los Datos personales, se procesa
			en las oficinas operativas de la Compañía y en cualquier otro lugar donde se encuentren las
			partes involucradas en el procesamiento. Significa que esta información puede transferirse y
			mantenerse en computadoras ubicadas fuera de su estado, provincia, país u otra jurisdicción
			gubernamental donde las leyes de protección de datos pueden diferir de las de su jurisdicción.
		</p>
		<p>Su consentimiento a esta Política de privacidad seguido de
			su envío de dicha información representa su aceptación de esa transferencia.</p>
		<p>La Compañía tomará todas las medidas razonablemente
			necesarias para garantizar que sus datos se traten de forma segura y de acuerdo con esta
			Política de privacidad y no se realizará ninguna transferencia de sus datos personales a una
			organización o país a menos que existan controles adecuados, incluida la seguridad de sus datos
			y otra información personal.</p>
		<h3>Eliminar sus datos personales</h3>
		<p>Tiene derecho a eliminar o solicitar que ayudemos a eliminar
			los Datos personales que hemos recopilado sobre usted.</p>
		<p>Nuestro Servicio puede darle la posibilidad de eliminar
			cierta información sobre Usted desde dentro del Servicio.</p>
		<p>Puede actualizar, modificar o eliminar su información en
			cualquier momento iniciando sesión en su cuenta, si tiene una, y visitando la sección de
			configuración de la cuenta que le permite administrar su información personal. También puede
			ponerse en contacto con nosotros para solicitar acceso, corregir o eliminar cualquier
			información personal que nos haya proporcionado.</p>
		<p>Tenga en cuenta, sin embargo, que es posible que
			necesitemos retener cierta información cuando tengamos una obligación legal o una base legal
			para hacerlo.</p>
		<h3>Divulgación de sus datos personales</h3>
		<h4>Transacciones comerciales</h4>
		<p>Si la Compañía está involucrada en una fusión, adquisición
			o venta de activos, sus datos personales pueden ser transferidos. Le notificaremos antes de que
			sus datos personales se transfieran y queden sujetos a una Política de privacidad diferente.</p>
		<h4>Aplicación de la ley</h4>
		<p>En determinadas circunstancias, la Compañía puede estar
			obligada a divulgar sus Datos personales si así lo exige la ley o en respuesta a solicitudes
			válidas de las autoridades públicas (por ejemplo, un tribunal o una agencia gubernamental).</p>
		<h4>Otros requisitos legales</h4>
		<p>La Compañía puede divulgar sus datos personales en la
			creencia de buena fe de que dicha acción es necesaria para:</p>
		<ul>
			<li>Cumplir con una obligación legal</li>
			<li>Proteger y defender los derechos o propiedad de la
				Compañía</li>
			<li>Prevenir o investigar posibles irregularidades en
				relación con el Servicio</li>
			<li>Proteger la seguridad personal de los Usuarios del
				Servicio o del público</li>
			<li>Protéjase contra la responsabilidad legal</li>
		</ul>
		<h3>Seguridad de sus datos personales</h3>
		<p>La seguridad de sus datos personales es importante para
			nosotros, pero recuerde que ningún método de transmisión a través de Internet o método de
			almacenamiento electrónico es 100% seguro. Si bien nos esforzamos por utilizar medios
			comercialmente aceptables para proteger sus datos personales, no podemos garantizar su seguridad
			absoluta.</p>
