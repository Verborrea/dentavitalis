		<h2>Privacidad de los niños</h2>
		<p>Nuestro Servicio no se dirige a ninguna persona menor de
			13 años. No recopilamos a sabiendas información de identificación personal de ninguna persona
			menor de 13 años. Si usted es un padre o tutor y sabe que su hijo nos ha proporcionado datos
			personales, póngase en contacto con nosotros. Si nos damos cuenta de que hemos recopilado datos
			personales de cualquier persona menor de 13 años sin verificación del consentimiento de los
			padres, tomamos medidas para eliminar esa información de nuestros servidores.</p>
		<p>Si necesitamos confiar en el consentimiento como base
			legal para procesar su información y su país requiere el consentimiento de un padre, podemos
			requerir el consentimiento de sus padres antes de recopilar y usar esa información.</p>
